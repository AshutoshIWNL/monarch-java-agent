package com.asm.mja.logging;

/**
 * The LogLevel enum represents different levels of logging.
 * Each level corresponds to a specific severity of message.
 *
 * TRACE:   Detailed tracing information.
 * DEBUG:   Debugging information.
 * INFO:    General information.
 * WARNING: Warning messages.
 * ERROR:   Error messages.
 *
 * These levels are used to control the verbosity of logging.
 *
 * @author ashut
 * @since 11-04-2024
 */

public enum LogLevel {
    TRACE,
    DEBUG,
    INFO,
    WARNING,
    ERROR
}
